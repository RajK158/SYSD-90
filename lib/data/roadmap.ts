// Generate a flat list of daily tasks from the roadmap
export function generateDailyTasks() {
  const days = []
  let dayCount = 1
  for (const week of ROADMAP_DATA) {
    for (let d = 1; d <= 7; d++) {
      if (dayCount > 90) break
      const isWeekend = d >= 6
      const dayOfWeek = ['Monday', 'Tuesday', 'Wednesday', 'Thursday', 'Friday', 'Saturday', 'Sunday'][d - 1]
      days.push({
        day: dayCount,
        week: week.week,
        dayOfWeek,
        isWeekend,
        systemDesignTask: isWeekend
          ? `Week ${week.week} review: ${week.topics[0]}`
          : week.topics[Math.min(d - 1, week.topics.length - 1)],
        dsaTask: isWeekend
          ? 'Review DSA mistakes from this week'
          : `${week.dsaFocus[Math.min(d - 1, week.dsaFocus.length - 1)]} practice`,
        practicalTask: isWeekend
          ? d === 6
            ? 'Build mini project or architecture diagram (2 hours)'
            : 'Full mock system design (45 min)'
          : week.practicalExercises[Math.min(d - 1, week.practicalExercises.length - 1)],
        revisionTask: isWeekend
          ? d === 7
            ? 'Update tracker, write weekly summary'
            : 'Revise DSA mistakes (1 hour)'
          : 'Revise yesterday\'s notes and key concepts (30 min)',
      })
      dayCount++
    }
  }
  return days
}
